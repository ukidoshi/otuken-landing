// Получить все активные кварталы
export const getActiveDistricts = () => {
  return Object.entries(districts)
    .filter(([_, district]) => district.status === 'active')
    .map(([id, district]) => ({ id, ...district }))
}

// Получить квартал по ID
export const getDistrictById = (id) => {
  return districts[id] ? { id, ...districts[id] } : null
}
